# 한글 음절 조합 상수(초성 19·중성 21·종성 28 — 유니코드 완성형 순서).
_JUNG = list("ㅏㅐㅑㅒㅓㅔㅕㅖㅗㅘㅙㅚㅛㅜㅝㅞㅟㅠㅡㅢㅣ")
