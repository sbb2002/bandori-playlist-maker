_JONG_SIOT = _JONG.index("ㅅ")
_JONG_NIEUN = _JONG.index("ㄴ")

# 장음(ー) 처리용 — 직전 음절 모음(중성)을 한 글자 더 반복할 때 쓸 홑글자.
_VOWEL_ECHO = {"ㅏ": "아", "ㅣ": "이", "ㅜ": "우", "ㅔ": "에", "ㅗ": "오", "ㅓ": "어", "ㅡ": "으"}


def _decompose(ch: str) -> tuple[int, int, int] | None:
    """완성형 한글 음절 1글자를 (초성,중성,종성) 인덱스로 분해. 한글이 아니면 None."""
    code = ord(ch) - 0xAC00
    if not (0 <= code < 11172):
        return None
    return code // (21 * 28), (code % (21 * 28)) // 28, code % 28


def _add_batchim(text: str, jong_idx: int) -> str:
    """`text`의 마지막 글자(받침 없는 한글 음절)에 종성을 얹는다.

    `text`는 외래어 사전 치환으로 여러 글자짜리 문자열(예: "텔레파시")일 수 있으므로,
    전체를 한 음절로 취급하지 않고 **마지막 한 글자만** 분해·재조합한다. 이미 받침이
    있거나 마지막 글자가 완성형 한글이 아니면 그대로 둔다(단순화).
    """
    if not text:
        return text
    parts = _decompose(text[-1])
    if parts is None:
        return text
    cho, jung, jong = parts
    if jong != 0:
        return text
    return text[:-1] + chr(0xAC00 + (cho * 21 + jung) * 28 + jong_idx)


def kana_to_hangul(hira: str) -> str:
    """히라가나 문자열 → 규칙기반 한글 음차(로마자 비경유).

    - 외래어 사전: 최장일치 우선으로 사전 단어를 찾아 매핑(글자 단위 규칙보다 우선).
    - 촉음(っ): 앞 음절에 ㅅ받침(있으면 덮어쓰지 않음).
    - 발음(ん): 앞 음절에 ㄴ받침(단순화 — b/m/p 앞 ㅁ받침 등 위치별 규칙 생략). 앞 음절이 없으면 "응".
    - 장음(ー): 직전 음절의 모음을 한 글자 반복.
    - 매핑에 없는 문자(칸지 잔존·영숫자·기호 등)는 원문 그대로 통과.
    """
    out: list[str] = []
    i, n = 0, len(hira)
    while i < n:
        # 외래어 사전 최장일치 시도 (이 위치부터 시작하는 가장 긴 부분 문자열 찾기)
        best_match = None
        best_len = 0
        for j in range(i + 1, n + 1):
            substr = hira[i:j]
            if substr in _LOANWORD_HANGUL:
                best_match = substr
                best_len = j - i

        if best_match:
            out.append(_LOANWORD_HANGUL[best_match])
            i += best_len
            continue

        ch = hira[i]
        nxt = hira[i + 1] if i + 1 < n else ""

        if ch == "っ":
            if out:
                out[-1] = _add_batchim(out[-1], _JONG_SIOT)
            i += 1
            continue

        if ch == "ん":
            if out:
                out[-1] = _add_batchim(out[-1], _JONG_NIEUN)
            else:
                out.append("응")
            i += 1
            continue

        if ch == "ー":
            if out:
                # 외래어 치환으로 out[-1]이 여러 글자일 수 있으므로 마지막 글자만 본다.
                parts = _decompose(out[-1][-1])
                if parts is not None:
                    echo = _VOWEL_ECHO.get(_JUNG[parts[1]])
                    if echo:
                        out.append(echo)
            i += 1
            continue

        digraph = ch + nxt
        if nxt and digraph in _DIGRAPHS:
            out.append(_DIGRAPHS[digraph])
            i += 2
            continue

        if ch in _MONOGRAPHS:
            out.append(_MONOGRAPHS[ch])
            i += 1
            continue

        out.append(ch)  # 매핑 밖 문자는 원문 통과(칸지 잔존 등)
        i += 1

    return "".join(out)


def to_romaji(title: str) -> str:
    """제목을 헵번식에 가까운 로마자로 변환한다(pykakasi, 칸지→히라가나→로마자)."""
    return "".join(item["hepburn"] for item in _kks.convert(title))


def to_hangul(title: str) -> str:
    """제목을 pykakasi 히라가나 단계에서 곧장 한글 음차로 변환한다(로마자 비경유)."""
    hira = "".join(item["hira"] for item in _kks.convert(title))
    return kana_to_hangul(hira)


def to_hanja_reading(title: str) -> str:
    """제목의 한자를 한국 한자음(음독)으로 변환한다.

    한자가 없는 제목은 원문 그대로 반환된다. hanja 라이브러리를 사용하여
    한자만 변환하고 가나·영숫자·기호는 원문 유지.
    """
    try:
        import hanja
        # 'substitution' 모드: 한자를 한국 한자음으로 치환, 그 외는 원문 통과
        return hanja.translate(title, "substitution")
    except ImportError:
        # hanja 미설치 시 원문 그대로 반환(fallback)
        return title
